
public class Player {
    private String name = "Player";
    private int Strenght = 10;
    private int HP = 100;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getStrenght() {
        return Strenght;
    }

    public void setStrenght(int strenght) {
        Strenght = strenght;
    }

    public int getHP() {
        return HP;
    }

    public void setHP(int hP) {
        HP = hP;
    }
}
